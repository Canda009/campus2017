package com.JimmyWang.springmvc;

public class Word {
	
	private String str;
    private int count;	
	private int enCount;
	private int cnCount;
	private int digitCount;
	private int punCount;
	
	public String getStr() {
		return str;
	}
	public void setStr(String str) {
		this.str = str;
	}
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getEnCount() {
		return enCount;
	}
	public void setEnCount(int enCount) {
		this.enCount = enCount;
	}
	public int getCnCount() {
		return cnCount;
	}
	public void setCnCount(int cnCount) {
		this.cnCount = cnCount;
	}
	public int getDigitCount() {
		return digitCount;
	}
	public void setDigitCount(int digitCount) {
		this.digitCount = digitCount;
	}
	public int getPunCount() {
		return punCount;
	}
	public void setPunCount(int pubCount) {
		this.punCount = pubCount;
	}
	
}
