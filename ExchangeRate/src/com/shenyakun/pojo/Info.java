package com.shenyakun.pojo;

public class Info {
	private String date;
	private String my;
	private String oy;
	private String gy;

	public String getDate() {
		return date;
	}

	public void setDate(String date) {
		this.date = date;
	}

	public String getMy() {
		return my;
	}

	public void setMy(String my) {
		this.my = my;
	}

	public String getOy() {
		return oy;
	}

	public void setOy(String oy) {
		this.oy = oy;
	}

	public String getGy() {
		return gy;
	}

	public void setGy(String gy) {
		this.gy = gy;
	}

}
